function buildProfileSection(p: TutorProfile): string {
  const mastered = p.masteredHebrew.length ? p.masteredHebrew.join(', ') : 'עדיין אין'
  const inProgress = p.inProgressHebrew.length ? p.inProgressHebrew.join(', ') : 'אין'
  const struggles = p.strugglesHebrew.length ? p.strugglesHebrew.join(', ') : 'אין דפוס מובהק'
  return `פרופיל סטודנט/ית:
- רמה: ${p.level}
- שולט/ת: ${mastered}
- בתהליך: ${inProgress}
- נקודות חולשה: ${struggles}`
}

function buildMSVSection(msv: TutorMSV): string {
  // Derive tone hint
  let tone = 'peer'
  if (msv.flowState === 'anxiety' || msv.confidence < 0.4) tone = 'restorative'
  else if (msv.flowState === 'boredom' && msv.confidence > 0.7) tone = 'coach'
  return `מצב מוטיבציה: flowState=${msv.flowState}, confidence=${msv.confidence.toFixed(2)}, primaryDeficit=${msv.primaryDeficit}
טון לפעולה: ${tone}`
}

function buildTurnSection(ctx: TutorTurnContext): string {
  const route = ctx.routeHe ?? 'לא ידוע'
  const topic = ctx.topicHe ?? 'אין'
  let quizLine = 'אין שאלת קוויז אחרונה.'
  if (ctx.lastQuizQuestion) {
    const correctness =
      ctx.lastQuizCorrect === null ? '?' : ctx.lastQuizCorrect ? 'נכונה' : 'שגויה'
    quizLine = `שאלת קוויז אחרונה: ${ctx.lastQuizQuestion}
תשובת סטודנט/ית: ${ctx.lastQuizUserAnswer ?? '—'} (${correctness})`
  }
  return `מסך נוכחי: ${route}
נושא פעיל: ${topic}
${quizLine}`
}

export function buildSystemPrompt(
  profile: TutorProfile,
  msv: TutorMSV,
  turn: TutorTurnContext,
): CachedBlock[] {
  return [
    // Section 1 — Persona (cached)
    { type: 'text', text: PERSONA, cache_control: { type: 'ephemeral' } },
    // Section 2 — Curriculum index (cached)
    { type: 'text', text: CURRICULUM_INDEX, cache_control: { type: 'ephemeral' } },
    // Section 6 — Tools usage (cached, placed early in stable prefix)
    { type: 'text', text: TOOLS_USAGE, cache_control: { type: 'ephemeral' } },
    // Section 3 — Student profile (cached, refreshes ~daily)
    { type: 'text', text: buildProfileSection(profile), cache_control: { type: 'ephemeral' } },
    // Section 4 — MSV (not cached, changes per session)
    { type: 'text', text: buildMSVSection(msv) },
    // Section 5 — Turn context (not cached, changes every turn)
    { type: 'text', text: buildTurnSection(turn) },
  ]
}

/** Convenience for the edge function / client — flatten to a plain string if SDK can't be loaded. */
export function flattenSystemPrompt(blocks: CachedBlock[]): string {
  return blocks.map((b) => b.text).join('\n\n')
}

