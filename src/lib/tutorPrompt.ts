/**
 * tutorPrompt.ts — 6-section cached system prompt builder.
 * Each section returns an Anthropic text block with optional cache_control,
 * so the backend can pass `system: buildSystemPrompt(...)` directly.
 *
 * Sections:
 *   1. PERSONA              — stable, cached
 *   2. CURRICULUM INDEX     — weekly refresh, cached
 *   3. STUDENT PROFILE      — daily refresh, cached
 *   4. MSV (motivation)     — per-session, NOT cached (changes often)
 *   5. TURN CONTEXT         — per-turn, NOT cached
 *   6. TOOLS USAGE          — stable, cached
 *
 * Cache breakpoints are placed after sections 1, 2, 3, 6 (mid+stable prefix).
 */

export interface TutorProfile {
  level: 'beginner' | 'intermediate' | 'advanced'
  masteredHebrew: string[] // hebrew titles
  inProgressHebrew: string[]
  strugglesHebrew: string[]
}

export interface TutorTurnContext {
  routeHe: string | null // "מסך לימוד — חציון"
  topicHe: string | null // "חציון"
  lastQuizQuestion: string | null
  lastQuizUserAnswer: string | null
  lastQuizCorrect: boolean | null
}

export interface TutorMSV {
  flowState: string // e.g. "neutral" | "anxiety" | "boredom"
  confidence: number // 0..1
  primaryDeficit: string // e.g. "capability" | "none"
}

interface CachedBlock {
  type: 'text'
  text: string
  cache_control?: { type: 'ephemeral' }
}

